function objType(name: string, item: ObjType): string[] {
  const res = ['', `struct ${name} {`];
  for (const [key, value] of Object.entries(item.d)) {
    const typeName = getTypeName(value);
    res.push(`  ${typeName} ${key};`);
  }
  res.push('};');
  const required = Object.entries(item.d).filter(([, v]) => !isOptionalType(v));
  const optional = Object.entries(item.d).filter(([, v]) => isOptionalType(v));
  headers.add('utility');
  addNonNamespace(`
#pragma region JSON serialization for object ${name}
template <> 
struct impl_to_json<${namespace}::${name}> { 
  static inline crow::json::wvalue process(
      const ${namespace}::${name}& _value) {
    crow::json::wvalue _res;
    ${required
      .map(([key]) => `_res["${key}"] = to_json(_value.${key});`)
      .join('\n    ')}
    ${optional
      .map(
        ([key]) => `if (_value.${key}) {
      _res["${key}"] = to_json(*_value.${key});
    }`,
      )
      .join('\n    ')}
    return _res;
  }
};

template <>
inline std::optional<${namespace}::${name}>
from_json<${namespace}::${name}>(
    const crow::json::rvalue& _value) {
  if (_value.t() != crow::json::type::Object)
    return std::nullopt;
  ${namespace}::${name} _res;
  ${required
    .map(
      ([key, value]) => `
  if (!_value.has("${key}"))
    return std::nullopt;
  auto _${key}_opt_ = from_json<${getTypeName(value, true)}>(_value["${key}"]);
  if (!_${key}_opt_.has_value())
    return std::nullopt;
  _res.${key} = std::move(*_${key}_opt_);`,
    )
    .join('\n  ')}
  ${optional
    .map(
      ([key, value]) => `
  if (_value.has("${key}")) {
    auto _${key}_opt_ = from_json<${getTypeName((value as OptType).d, true)}>(_value["${key}"]);
    if (!_${key}_opt_.has_value())
      return std::nullopt;
    _res.${key} = std::move(*_${key}_opt_);
  } else {
    _res.${key} = std::nullopt;
  }`,
    )
    .join('\n  ')}
  return _res;
}
#pragma endregion JSON serialization for object ${name}`);
  return res;
}

function subType(name: string, item: SubType): string[] {
  const res = ['', `struct ${name} : public ${item.p} {`];
  for (const [key, value] of Object.entries(item.d)) {
    const typeName = getTypeName(value);
    res.push(`  ${typeName} ${key};`);
  }
  res.push('};');
  headers.add('utility');
  addNonNamespace(`
#pragma region JSON serialization for object ${name}
template <> 
struct impl_to_json<${namespace}::${name}> { 
  static inline crow::json::wvalue process(
      const ${namespace}::${name}& _value) {
    crow::json::wvalue _res = impl_to_json<${namespace}::${item.p}>::process(_value);
    ${Object.entries(item.d)
      .map(([key]) => `_res["${key}"] = to_json(_value.${key});`)
      .join('\n    ')}
    return _res;
  }
};

template <>
inline std::optional<${namespace}::${name}>
from_json<${namespace}::${name}>(
    const crow::json::rvalue& _value) {
  if (_value.t() != crow::json::type::Object)
    return std::nullopt;
  std::optional<${namespace}::${item.p}> _base = from_json<${namespace}::${item.p}>(_value);
  if (!_base.has_value())
    return std::nullopt;  
  ${Object.entries(item.d)
    .map(
      ([key, value]) => `
  if (!_value.has("${key}"))
    return std::nullopt;
  auto _${key}_opt_ = from_json<${getTypeName(value, true)}>(_value["${key}"]);
  if (!_${key}_opt_.has_value())
    return std::nullopt;`,
    )
    .join('\n  ')}
  ${namespace}::${name} _res{std::move(*_base), ${
    // std::move the fields of the base, plus the inherited fields
    Object.entries(item.d)
      .map(([key]) => `std::move(*_${key}_opt_)`)
      .join(', ')
  }};

  return _res;
}
#pragma endregion JSON serialization for object ${name}`);
  return res;
}

function usingType(name: string, item: Types): string[] {
  // This is a base/simple type, so we don't need to do anything special,
  // just a simple using statement:
  return [`using ${name} = ${getTypeName(item)};`];
}

async function postProcess(code: string[]): Promise<string[]> {
  if (headerFileName.length > 0) {
    // For now, just write the header to the file:
    // Gotta use Node file stuff, because this thing may be running in either Bun or Node:
    await fsp.writeFile(
      headerFileName,
      `#pragma once

${[...headers]
  .sort()
  .map((nm) => `#include <${nm}>`)
  .join('\n')}
#include <crow/json.h>

${crow_header.replace(/@@@HEADER_NAME@@@/g, `${namespace}_pickling)}`)}`,
    );
  }
  return code;
}

export const CppEmitter: Emitter = {
  setInputFilename,
  setOutputFilename,
  setAdditionalOptions,
  generateHeader,
  generateFooter,
  types: {
    objType,
    subType,
    enumType,
    numEnumType,
    strEnumType,
    simpleType: usingType,
    optType: usingType,
    arrType: usingType,
    setType: usingType,
    fastSetType: usingType,
    mapType: usingType,
    fastMapType: usingType,
    tupType: usingType,
    strType: usingType,
  },
  postProcess,
};

export function GetCppGenerator(
  options?: Record<string, string>,
): IdlGenerator {
  // Returns the CppEmitter instance
  const emit = CppEmitter;
  if (options) {
    emit.setAdditionalOptions(options);
  }
  return MakeGenerator(CppEmitter);
}
