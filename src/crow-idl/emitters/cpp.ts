function generateHeader(): string[] {
  return `#pragma once
// DO NOT EDIT THIS FILE DIRECTLY!!!
// This file is generated from ${sourceFileName} by running 'crow-idl ...'.
// If you need to change this file, edit ${sourceFileName} and run
// the crow-idl command again. Or, if you need to change the generation process,
// put up a pull-request to https://github.com/kevinfrei/ts-cpp-tooling
// DO NOT EDIT THIS FILE DIRECTLY!!!

// Generated by ${process.env.npm_package_name} v${process.env.npm_package_version}

${[...headers]
  .sort()
  .map((nm) => `#include <${nm}>`)
  .join('\n')}

#include <crow/json.h>

// TODO: Configure this header to be an external blob
${headerFileName.length > 0 ? `#include "${headerFileName}"` : crow_header.replace(/@@@HEADER_NAME@@@/g, `${namespace}_pickling`)}
// TODO: End Configure this header to be an external blob

namespace ${namespace} {

template <typename T>
constexpr std::optional<T> from_string(std::string_view str);
`.split('\n');
}

const outsideNamespace: string[] = [];

function addNonNamespace(lines: string): void {
  outsideNamespace.push(lines);
}

function generateFooter(): string[] {
  const res = ['', `} // namespace ${namespace}`];
  res.push(...outsideNamespace);
  res.push('');
  return res;
}

function getTypeName(type: Types, scoped?: boolean): string {
  if (isU8Type(type)) {
    return 'std::uint8_t';
  } else if (isI8Type(type)) {
    return 'std::int8_t';
  } else if (isU16Type(type)) {
    return 'std::uint16_t';
  } else if (isI16Type(type)) {
    return 'std::int16_t';
  } else if (isU32Type(type)) {
    return 'std::uint32_t';
  } else if (isI32Type(type)) {
    return 'std::int32_t';
  } else if (isU64Type(type)) {
    return 'std::uint64_t';
  } else if (isI64Type(type)) {
    return 'std::int64_t';
  } else if (isPlainIntEnumType(type)) {
    return 'int';
  } else if (isStringType(type)) {
    return 'std::string';
  } else if (isCharType(type)) {
    return 'char';
  } else if (isBoolType(type)) {
    return 'bool';
  } else if (isFloatType(type)) {
    return 'float';
  } else if (isDoubleType(type)) {
    return 'double';
  } else if (isRefType(type)) {
    return scoped ? `${namespace}::${type.r}` : type.r; // Reference type, just return the name
  } else if (isArrayType(type)) {
    return `std::vector<${getTypeName(type.d, !!scoped)}>`;
  } else if (isSetType(type)) {
    return `std::set<${getTypeName(type.d, !!scoped)}>`;
  } else if (isFastSetType(type)) {
    return `std::unordered_set<${getTypeName(type.d, !!scoped)}>`;
  } else if (isMapType(type)) {
    return `std::map<${getTypeName(type.k, !!scoped)}, ${getTypeName(type.v, !!scoped)}>`;
  } else if (isFastMapType(type)) {
    return `std::unordered_map<${getTypeName(type.k, !!scoped)}, ${getTypeName(type.v, !!scoped)}>`;
  } else if (isTupleType(type)) {
    return `std::tuple<${type.l.map((a) => getTypeName(a, !!scoped)).join(', ')}>`;
  } else if (isOptionalType(type)) {
    return `std::optional<${getTypeName(type.d, !!scoped)}>`;
  }
  throw new Error(`Unsupported unnamed type: ${JSON.stringify(type)}`);
}

function enumType(name: string, item: Enum): string[] {
  headers.add('charconv');
  const typeName = getTypeName(item.u);
  const typeAttr = isPlainIntEnumType(item.u) ? '' : ` : ${typeName}`;
  return [
    `
#pragma region numeric enum ${name}
enum class ${name}${typeAttr} {
  ${item.v.join(',\n  ')}
};

inline constexpr bool is_valid(${name} _value) {
  switch (_value) {
${item.v.map((val) => `    case ${name}::${val}:`).join('\n')}
      return true;
    default:
      return false;
  }
}

inline constexpr std::string_view to_name(${name} _value) {
  switch (_value) {
${item.v
  .map(
    (val) => `    case ${name}::${val}:
      return "${val}";`,
  )
  .join('\n')}
    default:
      return "<unknown>";
  }
}

// Could be itoa with a cast, but string_view's...
inline constexpr std::string_view to_string(${name} _value) {
  switch (_value) {
${item.v
  .map(
    (val, idx) => `    case ${name}::${val}:
      return "${idx}";`,
  )
  .join('\n')}
    default:
      return "<unknown>";
  }
}

template <>
inline constexpr std::optional<${name}>
from_string<${name}>(std::string_view _str) {
  ${typeName} _val;
  auto [ptr, ec] = std::from_chars(_str.data(), _str.data() + _str.size(), _val);
  if (ec != std::errc{}) {
    return std::nullopt;
  }
  ${name} _res = static_cast<${name}>(_val);
  if (is_valid(_res))
    return _res;
  return std::nullopt;
}

#pragma endregion numeric enum ${name}`,
  ];
}

